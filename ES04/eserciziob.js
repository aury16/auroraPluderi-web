/**
 * ESERCIZIO B: Cicli
 * Obiettivo: Stampare i numeri da 100 a 0 con passo 20.
 */

// Utilizziamo un ciclo 'for'
// Inizializzazione: let i = 100 (partiamo da 100)
// Condizione: i >= 0 (continua finché i è maggiore o uguale a zero)
// Aggiornamento: i -= 20 (sottrae 20 al valore di i dopo ogni iterazione)

for (let i = 100; i >= 0; i -= 20) {
    // Stampiamo il valore corrente di i nella console
    console.log(i);
}
